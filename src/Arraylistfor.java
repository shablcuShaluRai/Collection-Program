import java.util.*;

public class Arraylistfor {

	public static void main(String[] args) {

		ArrayList<String> al = new ArrayList<String>();
		al.add("shalu");
		al.add("sahil");
		al.add("Ayush");
		al.add("Ram");
		// using traverse the value for each loop.

		for (String st : al) {
			System.out.println(st);
		}

	}

}
